CREATE DATABASE HR_data;
USE TSA;
select * from hr_data;
SELECT FIRST_NAME,LAST_NAME,SALARY FROM HR_DATA WHERE SALARY >6000;
SELECT FIRST_NAME,LAST_NAME,SALARY FROM hr_data WHERE SALARY < 8000;
SELECT FIRST_NAME,LAST_NAME,DEPARTMENT_ID FROM hr_data WHERE LAST_NAME LIKE "CHEN";
SELECT FIRST_NAME,LAST_NAME,DEPARTMENT_ID FROM HR_DATA WHERE DEPARTMENT_ID = 60 or 100;
SELECT FIRST_NAME,LAST_NAME,DEPARTMENT_ID FROM HR_DATA WHERE FIRST_NAME NOT LIKE  "%V%";
SELECT FIRST_Name,last_name,department_id FROM HR_DATA order BY department_id ASC ; 
SELECT CONCAT (FIRST_NAME,last_name,"-",department_id)  AS FULL_DETAILS FROM HR_DATA;
SELECT FIRST_NAME,LAST_NAME AS FULL_NAME,SALARY FROM HR_DATA WHERE COMMISSION_PCT IS NULL;
SELECT  FIRST_NAME,PHONE_NUMBER,SALARY FROM HR_DATA WHERE SALARY  BETWEEN 9000 AND 17000;
SELECT FIRST_NAME,LAST_NAME,SALARY FROM HR_DATA WHERE FIRST_NAME LIKE "%T";
SELECT  FIRST_NAME,LAST_NAME,SALARY FROM HR_DATA WHERE SALARY NOT  BETWEEN 7000 AND 15000 order by FIRST_NAME,LAST_NAME ASC;